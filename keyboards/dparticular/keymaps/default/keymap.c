static void update_leds(led_t led_state, bool num_layer_on)
{
    bool states[] = {
        num_layer_on,
        led_state.caps_lock,
        led_state.scroll_lock
    };
    uint8_t hue = rgblight_get_hue();
    uint8_t sat = rgblight_get_sat();
    uint8_t val = rgblight_get_val();

    uint8_t dhue = hue;
    uint8_t dsat = sat;
    uint8_t dval = val / 2;

    uint8_t ahue = hue + 256 / 2;
    uint8_t asat = sat;
    uint8_t aval = val;

    for (int i = 0; i < 3; i++) {
        if (states[i])
            sethsv(ahue, asat, aval, led + i);
        else
            sethsv(dhue, dsat, dval, led + i);
    }
    rgblight_set();
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static bool shifted = false;
    static bool shifted_8 = false;

    switch (keycode) {
        case KC_LSFT: case KC_RSFT:
            shifted = record->event.pressed;
            break;
        case KC_P8:
            if (record->event.pressed) {
                if (shifted) {
                    register_code(KC_PAST);
                    shifted_8 = true;
                    return false;
                }
            } else {
                if (shifted_8) {
                    unregister_code(KC_PAST);
                    shifted_8 = false;
                    return false;
                }
            }
            break;
        case RGB_HUI: case RGB_HUD:
        case RGB_SAI: case RGB_SAD:
        case RGB_VAI: case RGB_VAD:
            update_leds(host_keyboard_led_state(), IS_LAYER_ON(LAYER_NUM));
            break;
    }

	return true;
}

bool led_update_user(led_t led_state) {
    update_leds(led_state, IS_LAYER_ON(LAYER_NUM));

    return true;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    static bool num_layer_prev = false;
    bool num_layer_rising =
        !num_layer_prev && IS_LAYER_ON_STATE(state, LAYER_NUM);
    led_t led_state = host_keyboard_led_state();

    if (num_layer_rising && !led_state.num_lock) {
        SEND_STRING(SS_DOWN(X_NUMLOCK) SS_DELAY(100) SS_UP(X_NUMLOCK));
        update_leds(led_state, true);
    }

    return state;
}
